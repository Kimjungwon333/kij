package ch03;

public class oper {
	public static void main(String[] args) {
		int count = 0;
		//	count = 1;
		count += 1;//count = count +1;
		count = count +1;//count = count +1;
		count -= 1;//count = count -1;
		count ++;//count = count +1; 단 1씩만증가한다
		count --;//count = count +1; 단 1씩만 감소한다
		System.out.println(count);
		System.out.println(0-3);
	}

}
